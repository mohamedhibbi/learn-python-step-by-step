print(2 + 3)           # Addition (+): 2 + 3 = 5
print(2 * 4)           # Multiplication (*): 2 * 4 = 8
print(10 - 5)          # Subtraction (-): 10 - 5 = 5
print(20 / 5)          # Division (/): 20 / 5 = 4.0 (always returns a float)
print(3 ** 2)          # Exponent (**): 3 squared = 3 * 3 = 9
print(3 ** 7)          # 3 to the power of 7 = 2187
print(10 ** 3)         # 10 to the power of 3 = 1000
print(3 % 2)           # Modulus (%): remainder of 3 / 2, 1 if odd, 0 if even
print(10 // 2)         # Floor division (//): 10 divided by 2 = 5 (integer only)
print(3 // 2)          # Floor division: 3 // 2 = 1
